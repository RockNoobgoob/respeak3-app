/**
 * generate-ios.ts
 * ReSpeak3 BrandKit — iOS/SwiftUI code generator
 *
 * Reads token JSON files from ../tokens/ and writes three Swift source files
 * to ../generated/ios/:
 *   - BrandColors.swift
 *   - BrandTypography.swift
 *   - BrandLayout.swift
 *
 * Run:  npx ts-node scripts/generate-ios.ts
 */

import * as fs from 'fs';
import * as path from 'path';

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------

const TOKENS_DIR  = path.resolve(__dirname, '..', 'tokens');
const OUTPUT_DIR  = path.resolve(__dirname, '..', 'generated', 'ios');

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function readJSON(filename: string): Record<string, unknown> {
  const fullPath = path.join(TOKENS_DIR, filename);
  const raw = fs.readFileSync(fullPath, 'utf-8');
  return JSON.parse(raw) as Record<string, unknown>;
}

function ensureDir(dir: string): void {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
}

function write(filename: string, content: string): void {
  const fullPath = path.join(OUTPUT_DIR, filename);
  fs.writeFileSync(fullPath, content, 'utf-8');
  console.log(`  [ios] wrote ${path.relative(path.resolve(__dirname, '..', '..'), fullPath)}`);
}

/** Convert a 6-digit hex string to Swift Color(red:green:blue:) components. */
function hexToSwiftRGB(hex: string): string {
  const cleaned = hex.replace('#', '');
  const r = parseInt(cleaned.slice(0, 2), 16) / 255;
  const g = parseInt(cleaned.slice(2, 4), 16) / 255;
  const b = parseInt(cleaned.slice(4, 6), 16) / 255;
  return `Color(red: ${r.toFixed(4)}, green: ${g.toFixed(4)}, blue: ${b.toFixed(4)})`;
}

/** Swift-safe property name: "2xl" -> "size2XL", "900" -> "n900" etc. */
function toSwiftName(key: string, prefix: string = ''): string {
  // Numeric-only keys get a letter prefix to be valid Swift identifiers
  const safe = key.replace(/^(\d)/, '_$1');
  // kebab/dot to camelCase
  const camel = safe.replace(/[-.](\w)/g, (_, c: string) => c.toUpperCase());
  return prefix ? `${prefix}${camel.charAt(0).toUpperCase()}${camel.slice(1)}` : camel;
}

function header(filename: string, description: string, usageLines: string[]): string {
  const usageBlock = usageLines.map(l => ` //   ${l}`).join('\n');
  return `// ${filename}
// ReSpeak — generated by brandkit/scripts/generate-ios.ts
// DO NOT EDIT MANUALLY — run \`npm run gen:ios\` from /brandkit to regenerate.
//
// ${description}
//
// Usage:
${usageBlock}

import SwiftUI

`;
}

