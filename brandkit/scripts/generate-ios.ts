function generateTypography(typo: Record<string, unknown>): string {
  const lines: string[] = [];

  lines.push(header(
    'BrandTypography.swift',
    'Typography tokens for the HovaRehab / ReSpeak3 design system.',
    [
      'Text("Welcome").font(BrandTypography.display)',
      'Text("Section").font(BrandTypography.headline)',
      'Text("Details").brandStyle(.body)',
    ]
  ));

  const sizes       = typo['size']       as Record<string, string>;
  const weights     = typo['weight']     as Record<string, string>;
  const lineHeights = typo['lineHeight'] as Record<string, string>;
  const roles       = typo['roles']      as Record<string, Record<string, string>>;

  // Map token size keys to CGFloat names
  const sizeMap: Record<string, string> = {};
  for (const [k, v] of Object.entries(sizes)) {
    sizeMap[k] = v;
  }

  // Map weight strings to SwiftUI Font.Weight
  const weightMap: Record<string, string> = {
    '400': '.regular',
    '500': '.medium',
    '600': '.semibold',
    '700': '.bold',
  };

  lines.push(`// MARK: - BrandTypography\n`);
  lines.push(`/// Typographic scale for the ReSpeak / HovaRehab design system.`);
  lines.push(`/// All fonts use the system default (SF Pro) for Dynamic Type compatibility.`);
  lines.push(`public enum BrandTypography {\n`);

  lines.push(`    // MARK: - Semantic Role Fonts\n`);
  for (const [roleName, roleSpec] of Object.entries(roles)) {
    const sizePt  = sizeMap[roleSpec['size']] ?? '17';
    const weightW = weights[roleSpec['weight']] ?? '400';
    const weight  = weightMap[weightW] ?? '.regular';
    const desc    = roleSpec['description'] ?? '';
    const swiftName = roleName; // already camelCase in JSON

    lines.push(`    /// ${desc}`);
    lines.push(`    public static let ${swiftName}: Font = .system(size: ${sizePt}, weight: ${weight}, design: .default)`);
  }
  lines.push('');

  lines.push(`    // MARK: - Raw Size Scale\n`);
  for (const [k, v] of Object.entries(sizes)) {
    const safe = k.replace(/^(\d)/, '_$1');
    lines.push(`    /// ${v}pt`);
    lines.push(`    public static let size${safe.charAt(0).toUpperCase()}${safe.slice(1)}: CGFloat = ${v}`);
  }
  lines.push('');

  lines.push(`    // MARK: - Raw Weight Scale\n`);
  for (const [k, v] of Object.entries(weights)) {
    const swiftW = weightMap[v] ?? '.regular';
    lines.push(`    public static let weight${k.charAt(0).toUpperCase()}${k.slice(1)}: Font.Weight = ${swiftW}`);
  }
  lines.push('');

  // Convenience aliases to match legacy BrandTypography API
  lines.push(`    // MARK: - Legacy API Aliases (matches pre-existing BrandTypography)\n`);
  const legacyMap: [string, string][] = [
    ['largeTitle',  'display'],
    ['title1',      'title'],
    ['title2',      'subtitle'],
    ['title3',      'sectionTitle'],
    ['callout',     'callout'],
    ['subheadline', 'subheadline'],
    ['footnote',    'caption'],
    ['caption1',    'label'],
    ['caption2',    'label'],
    ['navTitle',    'navTitle'],
  ];
  for (const [alias, role] of legacyMap) {
    if (alias !== role) {
      lines.push(`    public static let ${alias}: Font = ${role}`);
    }
  }
  lines.push(`}`);
  lines.push('');

  // BrandTextStyle enum + modifier (preserve full API from existing file)
  lines.push(`// MARK: - BrandTextStyle\n`);
  lines.push(`/// Semantic text style pairings combining a font and its standard foreground color.`);
  lines.push(`public enum BrandTextStyle {`);
  const styleNames = [
    'display', 'title', 'subtitle', 'sectionTitle', 'headline',
    'body', 'callout', 'subheadline', 'caption', 'label',
    'button', 'navTitle', 'onPrimary',
  ];
  for (const s of styleNames) {
    lines.push(`    case ${s}`);
  }
  lines.push(`}`);
  lines.push('');

  lines.push(`// MARK: - BrandTextStyleModifier\n`);
  lines.push(`private struct BrandTextStyleModifier: ViewModifier {`);
  lines.push(`    let style: BrandTextStyle\n`);
  lines.push(`    func body(content: Content) -> some View {`);
  lines.push(`        switch style {`);

  const styleSpecs: Record<string, { font: string; color: string }> = {
    display:      { font: 'BrandTypography.display',      color: 'BrandColors.onBackground' },
    title:        { font: 'BrandTypography.title',        color: 'BrandColors.onBackground' },
    subtitle:     { font: 'BrandTypography.subtitle',     color: 'BrandColors.onBackground' },
    sectionTitle: { font: 'BrandTypography.sectionTitle', color: 'BrandColors.onBackground' },
    headline:     { font: 'BrandTypography.headline',     color: 'BrandColors.onBackground' },
    body:         { font: 'BrandTypography.body',         color: 'BrandColors.onBackground' },
    callout:      { font: 'BrandTypography.callout',      color: 'BrandColors.onBackgroundSecondary' },
    subheadline:  { font: 'BrandTypography.subheadline',  color: 'BrandColors.onBackgroundSecondary' },
    caption:      { font: 'BrandTypography.caption',      color: 'BrandColors.onBackgroundSecondary' },
    label:        { font: 'BrandTypography.label',        color: 'BrandColors.onBackgroundSecondary' },
    button:       { font: 'BrandTypography.button',       color: 'BrandColors.onBackground' },
    navTitle:     { font: 'BrandTypography.navTitle',     color: 'BrandColors.onBackground' },
    onPrimary:    { font: 'BrandTypography.body',         color: 'BrandColors.onPrimary' },
  };

  for (const [caseName, spec] of Object.entries(styleSpecs)) {
    lines.push(`        case .${caseName}:`);
    lines.push(`            content`);
    lines.push(`                .font(${spec.font})`);
    lines.push(`                .foregroundStyle(${spec.color})`);
  }
  lines.push(`        }`);
  lines.push(`    }`);
  lines.push(`}`);
  lines.push('');

  lines.push(`// MARK: - View Extension\n`);
  lines.push(`public extension View {`);
  lines.push(`    /// Applies a semantic brand text style (font + foreground color pairing).`);
  lines.push(`    ///`);
  lines.push(`    /// Example:`);
  lines.push(`    /// \`\`\`swift`);
  lines.push(`    /// Text("Hello").brandStyle(.headline)`);
  lines.push(`    /// \`\`\``);
  lines.push(`    func brandStyle(_ style: BrandTextStyle) -> some View {`);
  lines.push(`        self.modifier(BrandTextStyleModifier(style: style))`);
  lines.push(`    }`);
  lines.push(`}`);
  lines.push('');

  return lines.join('\n');
}

