function generateColors(colors: Record<string, unknown>): string {
  const lines: string[] = [];

  lines.push(header(
    'BrandColors.swift',
    'Color tokens for the HovaRehab / ReSpeak3 design system.',
    [
      'Text("Hello").foregroundStyle(BrandColors.brand800)',
      'Rectangle().fill(BrandColors.semanticSuccess)',
      'Color.brand800   // Color extension shorthand',
    ]
  ));

  // --- Color(hex:) helper extension ---
  lines.push(`// MARK: - Color(hex:) Helper`);
  lines.push(`
extension Color {
    /// Initialise a SwiftUI Color from a 6-digit hex string (e.g. "#1A3C5E").
    /// Alpha defaults to 1.0 (fully opaque).
    init(hex: String) {
        let hex = hex.trimmingCharacters(in: CharacterSet.alphanumerics.inverted)
        var int: UInt64 = 0
        Scanner(string: hex).scanHexInt64(&int)
        let r = Double((int >> 16) & 0xFF) / 255.0
        let g = Double((int >> 8)  & 0xFF) / 255.0
        let b = Double( int        & 0xFF) / 255.0
        self.init(red: r, green: g, blue: b)
    }
}
`);

  // --- BrandColors enum ---
  lines.push(`// MARK: - BrandColors\n`);
  lines.push(`/// All design-system color tokens, generated from brandkit/tokens/colors.json.`);
  lines.push(`public enum BrandColors {\n`);

  function emitGroup(groupKey: string, values: Record<string, string>, sectionComment: string): void {
    lines.push(`    // MARK: - ${sectionComment}\n`);
    for (const [tokenKey, hex] of Object.entries(values)) {
      if (hex === 'transparent') {
        const propName = toSwiftName(`${groupKey}${tokenKey.charAt(0).toUpperCase()}${tokenKey.slice(1)}`);
        lines.push(`    /// Fully transparent — use for no-fill / clear backgrounds.`);
        lines.push(`    public static let ${propName}: Color = .clear`);
      } else {
        const propName = toSwiftName(`${groupKey}${tokenKey.charAt(0).toUpperCase()}${tokenKey.slice(1)}`);
        lines.push(`    public static let ${propName}: Color = ${hexToSwiftRGB(hex)}  // ${hex}`);
      }
    }
    lines.push('');
  }

  const brand   = colors['brand']   as Record<string, string>;
  const surface = colors['surface'] as Record<string, string>;
  const text    = colors['text']    as Record<string, string>;
  const semantic = colors['semantic'] as Record<string, string>;
  const accent  = colors['accent']  as Record<string, string>;
  const fixed   = colors['fixed']   as Record<string, string>;

  emitGroup('brand',   brand,   'Brand Scale — brand.50 → brand.900');
  emitGroup('surface', surface, 'Surface Scale — surface.0 → surface.300');
  emitGroup('text',    text,    'Text Scale — text.300 → text.900');

  // Semantic with light variants grouped
  lines.push(`    // MARK: - Semantic States\n`);
  for (const [tokenKey, hex] of Object.entries(semantic)) {
    const propName = `semantic${tokenKey.charAt(0).toUpperCase()}${tokenKey.slice(1)}`;
    lines.push(`    public static let ${propName}: Color = ${hexToSwiftRGB(hex)}  // ${hex}`);
  }
  lines.push('');

  emitGroup('accent', accent, 'Accent');
  emitGroup('fixed',  fixed,  'Fixed (White / Black / Transparent)');

  // Semantic convenience aliases matching original BrandColors API
  lines.push(`    // MARK: - Semantic Convenience Aliases (matches pre-existing BrandColors API)\n`);
  lines.push(`    /// Main brand / primary interactive color.`);
  lines.push(`    public static let primary:               Color = brand800`);
  lines.push(`    /// Supporting brand color.`);
  lines.push(`    public static let secondary:             Color = brand500`);
  lines.push(`    /// Teal accent / CTA highlight.`);
  lines.push(`    public static let accent:                Color = accentPrimary`);
  lines.push(`    /// Root page background.`);
  lines.push(`    public static let background:            Color = surface50`);
  lines.push(`    /// Card / panel surface.`);
  lines.push(`    public static let surface:               Color = surface0`);
  lines.push(`    /// Slightly tinted alternate surface.`);
  lines.push(`    public static let surfaceVariant:        Color = surface100`);
  lines.push(`    /// Text / icon on primary-colored backgrounds.`);
  lines.push(`    public static let onPrimary:             Color = fixedWhite`);
  lines.push(`    /// Primary text on light backgrounds.`);
  lines.push(`    public static let onBackground:          Color = text900`);
  lines.push(`    /// Secondary / muted text.`);
  lines.push(`    public static let onBackgroundSecondary: Color = text500`);
  lines.push(`    /// Error / destructive state.`);
  lines.push(`    public static let error:                 Color = semanticDanger`);
  lines.push(`    /// Success state.`);
  lines.push(`    public static let success:               Color = semanticSuccess`);
  lines.push(`    /// Warning state.`);
  lines.push(`    public static let warning:               Color = semanticWarning`);
  lines.push(`    /// Informational state.`);
  lines.push(`    public static let info:                  Color = semanticInfo`);
  lines.push(`    /// Subtle border and separator color.`);
  lines.push(`    public static let border:                Color = surface200`);
  lines.push(`    /// Disabled text and control color.`);
  lines.push(`    public static let disabled:              Color = surface300`);
  lines.push(`}`);
  lines.push('');

  // --- Color extension shortcuts ---
  lines.push(`// MARK: - Color Static Shorthand Extensions\n`);
  lines.push(`/// Provides \`Color.brand800\`, \`Color.semanticSuccess\` etc. shorthand.`);
  lines.push(`public extension Color {`);
  const allGroups: [string, Record<string, string>][] = [
    ['brand', brand], ['surface', surface], ['text', text], ['accent', accent], ['fixed', fixed],
  ];
  for (const [groupKey, values] of allGroups) {
    for (const tokenKey of Object.keys(values)) {
      const propName = toSwiftName(`${groupKey}${tokenKey.charAt(0).toUpperCase()}${tokenKey.slice(1)}`);
      lines.push(`    static var ${propName}: Color { BrandColors.${propName} }`);
    }
  }
  lines.push(`}`);
  lines.push('');

  return lines.join('\n');
}

