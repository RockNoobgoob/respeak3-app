function generateLayout(layout: Record<string, unknown>): string {
  const lines: string[] = [];

  lines.push(header(
    'BrandLayout.swift',
    'Spacing, border radius, shadow, and border tokens for the HovaRehab / ReSpeak3 design system.',
    [
      '.padding(Spacing.lg)                      // 16 pt',
      'VStack(spacing: Spacing.xl) { ... }       // 24 pt',
      '.clipShape(RoundedRectangle(cornerRadius: Radii.card))',
      '.cardShadow()  // view modifier shorthand',
    ]
  ));

  const spacing      = layout['spacing']          as Record<string, string>;
  const spAliases    = layout['spacingAliases']   as Record<string, string>;
  const radii        = layout['borderRadius']     as Record<string, string>;
  const radiiAliases = layout['borderRadiusAliases'] as Record<string, string>;
  const borders      = layout['borderWidth']      as Record<string, string>;
  const shadows      = layout['shadow']           as Record<string, Record<string, string>>;
  const shadAliases  = layout['shadowAliases']    as Record<string, string>;

  // --- Spacing ---
  lines.push(`// MARK: - Spacing\n`);
  lines.push(`/// Spacing scale for the ReSpeak / HovaRehab design system.`);
  lines.push(`/// Based on a 4-pt base unit to align with iOS HIG recommendations.`);
  lines.push(`public enum Spacing {\n`);
  for (const [k, v] of Object.entries(spacing)) {
    // Convert "2xl" -> "xxl" style for Swift (avoid leading-digit identifiers)
    const swiftKey = k.replace(/^(\d+)xl$/, (_, n: string) => 'x'.repeat(parseInt(n, 10)) + 'l');
    lines.push(`    /// ${v} pt`);
    lines.push(`    public static let ${swiftKey}: CGFloat = ${v}`);
  }
  lines.push('');
  lines.push(`    // MARK: Semantic Aliases\n`);
  for (const [alias, ref] of Object.entries(spAliases)) {
    const swiftRef = ref.replace(/^(\d+)xl$/, (_, n: string) => 'x'.repeat(parseInt(n, 10)) + 'l');
    lines.push(`    public static let ${alias}: CGFloat = ${swiftRef}`);
  }
  lines.push(`}`);
  lines.push('');

  // --- Radii ---
  lines.push(`// MARK: - Radii\n`);
  lines.push(`/// Corner radius tokens for the ReSpeak / HovaRehab design system.`);
  lines.push(`public enum Radii {\n`);
  for (const [k, v] of Object.entries(radii)) {
    const swiftKey = k.replace(/^(\d+)xl$/, (_, n: string) => 'x'.repeat(parseInt(n, 10)) + 'l');
    lines.push(`    /// ${v} pt`);
    lines.push(`    public static let ${swiftKey}: CGFloat = ${v}`);
  }
  lines.push('');
  lines.push(`    // MARK: Semantic Aliases\n`);
  for (const [alias, ref] of Object.entries(radiiAliases)) {
    const swiftRef = ref.replace(/^(\d+)xl$/, (_, n: string) => 'x'.repeat(parseInt(n, 10)) + 'l');
    lines.push(`    public static let ${alias}: CGFloat = ${swiftRef}`);
  }
  lines.push(`}`);
  lines.push('');

  // --- BorderWidth ---
  lines.push(`// MARK: - BorderWidth\n`);
  lines.push(`/// Border / stroke width tokens.`);
  lines.push(`public enum BorderWidth {\n`);
  for (const [k, v] of Object.entries(borders)) {
    // 'default' is a Swift keyword — escape it with backticks
    const swiftKey = k === 'default' ? '`default`' : k;
    lines.push(`    /// ${v} pt`);
    lines.push(`    public static let ${swiftKey}: CGFloat = ${v}`);
  }
  lines.push(`}`);
  lines.push('');

  // --- Shadows (struct approach for grouping) ---
  lines.push(`// MARK: - Shadows\n`);
  lines.push(`/// Shadow elevation tokens. Each level groups color, radius, x, y, and opacity.`);
  lines.push(`public enum Shadows {\n`);
  lines.push(`    public struct ShadowToken {`);
  lines.push(`        public let color: Color`);
  lines.push(`        public let radius: CGFloat`);
  lines.push(`        public let x: CGFloat`);
  lines.push(`        public let y: CGFloat`);
  lines.push(`    }\n`);

  for (const [level, spec] of Object.entries(shadows)) {
    const swiftKey = level.replace(/^(\d+)xl$/, (_, n) => 'x'.repeat(parseInt(n, 10)) + 'l');
    if (spec['color'] === 'transparent') {
      lines.push(`    public static let ${swiftKey}: ShadowToken = ShadowToken(`);
      lines.push(`        color: .clear, radius: 0, x: 0, y: 0`);
      lines.push(`    )`);
    } else {
      const opacity = parseFloat(spec['opacity'] ?? '0.10');
      lines.push(`    public static let ${swiftKey}: ShadowToken = ShadowToken(`);
      lines.push(`        color: ${hexToSwiftRGB(spec['color'])}.opacity(${opacity}),`);
      lines.push(`        radius: ${spec['radius']}, x: ${spec['x']}, y: ${spec['y']}`);
      lines.push(`    )`);
    }
    lines.push('');
  }

  lines.push(`    // MARK: Semantic Aliases\n`);
  for (const [alias, ref] of Object.entries(shadAliases)) {
    const swiftRef = ref.replace(/^(\d+)xl$/, (_, n: string) => 'x'.repeat(parseInt(n, 10)) + 'l');
    lines.push(`    public static let ${alias}: ShadowToken = ${swiftRef}`);
  }
  lines.push('');

  // Legacy flat property aliases (matches Shadows.cardColor pattern)
  lines.push(`    // MARK: Legacy Flat Property Aliases (matches pre-existing Shadows API)\n`);
  lines.push(`    public static let cardColor:     Color   = sm.color`);
  lines.push(`    public static let cardRadius:    CGFloat = sm.radius`);
  lines.push(`    public static let cardX:         CGFloat = sm.x`);
  lines.push(`    public static let cardY:         CGFloat = sm.y`);
  lines.push(`    public static let elevatedColor:  Color   = md.color`);
  lines.push(`    public static let elevatedRadius: CGFloat = md.radius`);
  lines.push(`    public static let elevatedX:      CGFloat = md.x`);
  lines.push(`    public static let elevatedY:      CGFloat = md.y`);
  lines.push(`    public static let floatingColor:  Color   = lg.color`);
  lines.push(`    public static let floatingRadius: CGFloat = lg.radius`);
  lines.push(`    public static let floatingX:      CGFloat = lg.x`);
  lines.push(`    public static let floatingY:      CGFloat = lg.y`);
  lines.push(`    public static let noneColor:      Color   = none.color`);
  lines.push(`    public static let noneRadius:     CGFloat = none.radius`);
  lines.push(`    public static let noneX:          CGFloat = none.x`);
  lines.push(`    public static let noneY:          CGFloat = none.y`);
  lines.push(`}`);
  lines.push('');

  // Shadow view modifiers
  lines.push(`// MARK: - Shadow ViewModifiers\n`);
  lines.push(`private struct TokenShadowModifier: ViewModifier {`);
  lines.push(`    let token: Shadows.ShadowToken`);
  lines.push(`    func body(content: Content) -> some View {`);
  lines.push(`        content.shadow(color: token.color, radius: token.radius, x: token.x, y: token.y)`);
  lines.push(`    }`);
  lines.push(`}\n`);
  lines.push(`public extension View {`);
  lines.push(`    /// Applies the resting card shadow (sm elevation).`);
  lines.push(`    func cardShadow() -> some View { modifier(TokenShadowModifier(token: Shadows.sm)) }`);
  lines.push(`    /// Applies the elevated / active-state shadow (md elevation).`);
  lines.push(`    func elevatedShadow() -> some View { modifier(TokenShadowModifier(token: Shadows.md)) }`);
  lines.push(`    /// Applies the floating / modal shadow (lg elevation).`);
  lines.push(`    func floatingShadow() -> some View { modifier(TokenShadowModifier(token: Shadows.lg)) }`);
  lines.push(`}`);
  lines.push('');

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main(): void {
  console.log('ReSpeak3 BrandKit — iOS generator');
  console.log('----------------------------------');

  ensureDir(OUTPUT_DIR);

  const colors = readJSON('colors.json');
  const typo   = readJSON('typography.json');
  const layout = readJSON('layout.json');

  write('BrandColors.swift',    generateColors(colors));
  write('BrandTypography.swift', generateTypography(typo));
  write('BrandLayout.swift',    generateLayout(layout));

  console.log('\nDone. Copy files to ReSpeak/Theme/ or run scripts/sync-brand.sh.\n');
}

main();
